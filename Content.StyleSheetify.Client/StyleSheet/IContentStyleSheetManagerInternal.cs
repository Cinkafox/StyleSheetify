namespace Content.StyleSheetify.Client.StyleSheet;

/// <summary>
/// Внутренний интерфейс жизненного цикла менеджера стилей.
/// Предназначен для вызова этапов инициализации системой, но не для использования внешними компонентами.
/// Разделение на <see cref="Initialize"/> и <see cref="PostInitialize"/> позволяет
/// сначала зарегистрировать зависимости, а затем выполнить логику, требующую полной загрузки прототипов.
/// </summary>
internal interface IContentStyleSheetManagerInternal
{
    /// <summary>
    /// Выполняет раннюю инициализацию: получает зависимости через DI, создаёт логгер, подписывается на события.
    /// Вызывается сразу после создания экземпляра менеджера.
    /// </summary>
    public void Initialize();

    /// <summary>
    /// Выполняет позднюю инициализацию после полной загрузки всех прототипов.
    /// Здесь можно безопасно обращаться к данным прототипов (например, строить дерево наследования стилей).
    /// Вызывается после <see cref="Initialize"/> и после загрузки всех прототипов.
    /// </summary>
    public void PostInitialize();
}
